"""
    Python is dynamically typed language. 
    
    type():
        Function used to check data type
    Syntax:
        type(check_object)
    Return:
        class type of the argument
"""
# x holding 'int' type of data
x = 10
print(type(x))

# x holding 'float' type of data
x = 30.55
print(type(x))

# x holding 'complex' type of data
x = 20+30j
print(type(x))

# x holding 'bool' type of data
x = True
print(type(x))

# x holding 'str' type of data
x = 'Python'
print(type(x))

# None type
x = None
print(type(x))
